import math

angulo = float(input("Digite o valor do angulo: "))

print(f"O angulo de {angulo} tem o SENO de {math.sin(math.radians(angulo)):.2f}")
print(f"O angulo de {angulo} tem o SENO de {math.cos(math.radians(angulo)):.2f}")
print(f"O angulo de {angulo} tem o SENO de {math.tan(math.radians(angulo)):.2f}")